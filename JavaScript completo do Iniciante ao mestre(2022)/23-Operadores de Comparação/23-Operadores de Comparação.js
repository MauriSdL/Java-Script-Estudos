//- Operadores de Comparação
/*
    == igualdade de valor
    === igualdade de valor e tipo
    < menor que
    > maior que
    <= menor ou igual
    >= maior ou igual
    != valores diferentes
    !== valores e tipos diferentes
*/

let n = '25';
let n2 = 2;
console.log(n == n2);
console.log(n === n2);
console.log(n < n2);
console.log(n > n2);
console.log(n <= n2);
console.log(n >= n2);
console.log(n != n2);
console.log(n !== n2);
