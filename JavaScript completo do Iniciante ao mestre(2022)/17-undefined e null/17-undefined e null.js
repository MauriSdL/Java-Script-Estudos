// tipos primitivos
// undefined, null, symbol ( ES6 2015)

/*
-- undefined --
Significa que a variavel foi declarada,
mas nao foi definido nenhum valor pra ela
EX:
let varTeste;
console.log(varTeste);
console.log(typeof varTeste);
varTeste = 10;
console.log(typeof varTeste, varTeste);
*/

/*
-- null --
Serve para informar ao javaScript
que a variavel crada ainda nao possui um valor
*/
let nome = null;
console.log(nome);
console.log(typeof nome);
nome = 'Mauri'
console.log(typeof nome, nome);

